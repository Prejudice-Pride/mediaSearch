def build_dict() -> dict:
    """构建字典 (按地名长度降序, 便于最长匹配)。"""
    d = {}
    for name, lng, lat, prov, level in GEO_ENTRIES:
        d[name] = {
            "lng": float(lng),
            "lat": float(lat),
            "province": prov,
            "level": level,
        }
    return d


def main() -> None:
    ensure_dirs()
    d = build_dict()
    with GEO_DICT_FILE.open("w", encoding="utf-8") as f:
        json.dump(d, f, ensure_ascii=False, indent=2)

    # 统计级别
    lvl = {}
    for v in d.values():
        lvl[v["level"]] = lvl.get(v["level"], 0) + 1
    print(f"[OK] 已生成本地地名库 -> {GEO_DICT_FILE}")
    print(f"     词条总数: {len(d)}")
    print(f"     级别分布: {lvl}")


if __name__ == "__main__":
    main()
